package program;

/**
 *
 * @author ACER
 */
public class Resource {
    private String nama;
    private int jumlah;

    public Resource(String nama, int jumlah) {
        this.nama = nama;
        this.jumlah = jumlah;
    }

    public String getNama() {
        return nama;
    }

    public int getJumlah() {
        return jumlah;
    }

    public void setJumlah(int jumlah) {
        this.jumlah = jumlah;
    }
}
