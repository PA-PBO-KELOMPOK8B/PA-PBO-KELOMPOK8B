    private void loadData() {
        model.setRowCount(0); // Membersihkan tabel

        // Query untuk menggabungkan tabel peminjaman dan ruangan
        String query = "SELECT ruangan.id_ruangan, ruangan.nama, peminjaman.status " +
                       "FROM ruangan " +
                       "LEFT JOIN peminjaman ON ruangan.id_ruangan = peminjaman.id_ruangan";

        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement stmt = conn.prepareStatement(query);
             ResultSet rs = stmt.executeQuery()) {

            while (rs.next()) {
                String idRuangan = rs.getString("id_ruangan");
                String namaRuangan = rs.getString("nama");
                String status = rs.getString("status");

                // Tambahkan data ke tabel
                Object[] row = {idRuangan, namaRuangan, status};
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
        }
    }



    private void filterTable(String keyword) {
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        tabel_crud_ruang.setRowSorter(sorter);

        if (keyword.isEmpty()) {
            sorter.setRowFilter(null);
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + keyword));
        }
    }

