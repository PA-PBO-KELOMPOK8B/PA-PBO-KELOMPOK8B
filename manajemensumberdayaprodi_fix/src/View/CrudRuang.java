    private void b_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_tambahActionPerformed
        // TODO add your handling code here:
        String kodeRuangan = t_koderuang.getText().trim();
        String tipeRuangan = t_tipe.getText().trim();
        String ketersediaan = combox_ruang.getSelectedItem().toString();

        if (kodeRuangan.isEmpty() || tipeRuangan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String query = "INSERT INTO peminjaman (id_ruangan, nama, status) VALUES (?, ?, ?)";

        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement stmt = conn.prepareStatement(query)) {

            stmt.setString(1, kodeRuangan);
            stmt.setString(2, tipeRuangan);
            stmt.setString(3, ketersediaan);
            stmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
            loadData(); // Refresh tabel
            clearFields(); // Bersihkan input
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menambah data: " + e.getMessage());
        }
    }//GEN-LAST:event_b_tambahActionPerformed

